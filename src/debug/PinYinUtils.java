    public static String getPinYinT(String input) {
        ArrayList<FuzzyMatchHanziToPinyin.Token> tokens = FuzzyMatchHanziToPinyin.getInstance().getT(
                input);
        StringBuilder sb = new StringBuilder();
        if (tokens != null && tokens.size() > 0) {
            for (FuzzyMatchHanziToPinyin.Token token : tokens) {
                if (FuzzyMatchHanziToPinyin.Token.PINYIN == token.type) {
                    sb.append(token.target.charAt(0)+token.target.substring(1).toLowerCase()+" ");
                } else {
                    sb.append(token.source);
                }
            }
        }
        return sb.toString();
    }

